/*
 *  this file contains all data types used in UART drive for atmega32
 */ 


#ifndef UART_TYPES_H_
#define UART_TYPES_H_


typedef unsigned char uint8;
typedef signed char sint8;

typedef signed char sint16;
typedef unsigned int uint16;

typedef unsigned char* uint8_p;
typedef signed char* sint8_p;

typedef volatile unsigned char* uint8_vp;


#endif /* UART_TYPES_H_ */